#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char *d;
	unsigned char *s;

	d = dest;
	s = (void*)src;
	while (n--)
		*d++ = *s++;
	return (dest);
}
