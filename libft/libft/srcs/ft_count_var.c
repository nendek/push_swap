#include "libft.h"

int			ft_count_var(long long var)
{
	int		i;

	i = 0;
	if (var < 0)
		i++;
	if (var == 0)
		return (1);
	while (var != 0)
	{
		var /= 10;
		i++;
	}
	return (i);
}
